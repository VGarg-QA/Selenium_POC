package Inheritance.Polymorphism;

//When we want to stOre one type of value into another type we write that type infront
// of the value to be stored in round brackets this is called as type casting
public class Casting {

    public static void main(String[] args) {

        int a = 10;
        int b = 3;

        double c = (double) a/b; // Type casting

        System.out.println("c= " +c);
    }

}
