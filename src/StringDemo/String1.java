package StringDemo;

public class String1 {
    public static void main(String[] args) {

        String str1="this is a java"; // find character at index 3 & length
        System.out.println("length= "+ str1.length());
        System.out.println("Char at index 3= " + str1.charAt(3));

        // find character at index 2 & length
        String str2="hi i am here";
        System.out.println("length= "+ str2.length());
        System.out.println("Char at index 2= " + str2.charAt(2));

        // find character at index 0 & length
        String str3="where are you";
        System.out.println("length= "+ str3.length());
        System.out.println("Char at index 0= " + str3.charAt(0));

        // find character at index 5 & length
        String str4="this is a demo";
        System.out.println("length= "+ str4.length());
        System.out.println("Char at index 5= " + str4.charAt(5));

        String str5="core java";// find character at index 6 & length
        System.out.println("length= "+ str5.length());
        System.out.println("Char at index 6= " + str5.charAt(6));

    }
}
